function Accordion({ data }) {
  return (
    <div className="accordion">
      {data.map((item, index) => (
        <Items item={item} index={index} key={index} />
      ))}
    </div>
  );
}

function Items({ item, index }) {
  const [clicked, setClicked] = useState(false);
  function handleClick() {
    setClicked((clicked) => !clicked);
  }
  return (
    <div
      className={`item ${clicked ? "open" : ""}`}
      onClick={() => handleClick()}
    >
      <span className="number">{index >= 9 ? index + 1 : `0${index + 1}`}</span>
      <p className="title">{item.title}</p>
      <span className="icon"> {clicked ? "-" : "+"}</span>
      {clicked && <div className="content-box">{item.text}</div>}
    </div>
  );
}
